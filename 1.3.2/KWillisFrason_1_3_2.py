def add_tip(total, tip_percent):
    tip = tip_percent*total
    return total + tip
def hyp(leg1, leg2):
    sides = leg1**2 + leg2**2
    hyp = sides**0.5
    return hyp
def mean(a,b,c):
    total = a + b + c
    mean = total/3.0
    return mean
def perimeter(base, height):
    b = base*2
    h = height*2
    perimeter = b + h
    return perimeter